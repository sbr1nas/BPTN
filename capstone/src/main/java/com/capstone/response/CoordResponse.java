package com.capstone.response;

public class CoordResponse {

	private Double lon;
	private Double lat;
	
	public Double getLon() {
		return this.lon;
	}
	public void setLon(Double lon) {
		this.lon = lon;
	}
	public Double getLat() {
		return this.lat;
	}
	public void setLat(Double lat) {
		this.lat = lat;
	} 	

}
